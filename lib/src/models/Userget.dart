class User{
  static int id;

  static String name;

  static String last_name;

  static String gender;

  static String phone;

  static String birthday;

  void set setUser_id(int value){
    id = value;
  }
  int get getUser_id{
    return id;
  }
  void set setName(String value){
    name = value;
  }
  String get getName{
    return name;
  }
  void set setLast_Name(String value){
    last_name = value;
  }
  String get getLast_Name{
    return last_name;
  }

  void set setGender(String value){
    gender = value;
  }
  String get getGender{
    return gender;
  }
  void set setPhone(String value){
    phone = value;
  }
  String get getPhone{
    return phone;
  }

  void set setBirthday(String value){
    birthday = value;
  }
  String get getBirthday{
    return birthday;
  }





}